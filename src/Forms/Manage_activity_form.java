    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int rowIndex = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        //Object getValueAt(rowIndex,columnIndex)
        jTextField2.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField3.setText(model.getValueAt(rowIndex, 1).toString());

        String strDate = model.getValueAt(rowIndex, 2).toString();
        java.sql.Date sqlDate = java.sql.Date.valueOf(strDate);
        jDateChooser1.setDate(sqlDate);

        jTextField4.setText(model.getValueAt(rowIndex, 3).toString());
        jTextArea1.setText(model.getValueAt(rowIndex, 4).toString());

        if(model.getValueAt(rowIndex, 5).toString().equals("本社团成员")){
            jRadioButton2.setSelected(true);
            jRadioButton3.setSelected(false);
        }else if(model.getValueAt(rowIndex, 5).toString().equals("全体学生")){
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(true);
        }

        jTextField6.setText(model.getValueAt(rowIndex, 6).toString());
        String status = model.getValueAt(rowIndex, 7).toString();
        if(status.equals("0")){
            jRadioButton1.setSelected(true);
            jRadioButton4.setSelected(false);
            jRadioButton5.setSelected(false);
        }if(status.equals("1")){
            jRadioButton1.setSelected(false);
            jRadioButton4.setSelected(true);
            jRadioButton5.setSelected(false);
        }if(status.equals("2")){
            jRadioButton1.setSelected(false);
            jRadioButton4.setSelected(false);
            jRadioButton5.setSelected(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String aname = jTextField2.getText();
        if (MyFunction.if_acticity_exist(aname)) {
            if (getRange(aname).equals("本社团成员")) {
                if (MyFunction.if_student_is_in_club(sname, aname)) {
                    if (MyFunction.rowCount("participant") < getMaxNum(aname)) { //insert participant
                        participantInsert(sname,sno,aname);
                    } else {
                        JOptionPane.showMessageDialog(null, "活动人数已满");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "你不在活动范围内");
                }
            } else { //全体成员
                if (MyFunction.rowCount("participant") < getMaxNum(aname)) {  // insert participant
                    participantInsert(sname,sno,aname);
                } else {
                    JOptionPane.showMessageDialog(null, "活动人数已满");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "你输入的活动名不存在");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

