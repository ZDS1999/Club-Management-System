    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection conn = MyConnection.getConncetion();
        PreparedStatement ps = null;
        try {
            String name = jTextField2.getText();
            ps = conn.prepareStatement("UPDATE `activity` SET "
                    + "`AName`=?,`AHost`=?,`ATime`=?,`Address`=?,`AContent`=?,`Range`=?,`Max_num`=?,`AStatus`=? WHERE AName = ?");
            ps.setString(1, jTextField2.getText());
            ps.setString(2, jTextField3.getText());
            
            java.util.Date date = jDateChooser1.getDate();
            String strDate = DateFormat.getDateInstance().format(date);
            ps.setString(3, strDate);
            
            ps.setString(4, jTextField4.getText());
            ps.setString(5, jTextArea1.getText());
            
            String range = null;
                if(jRadioButton2.isSelected()){
                    range = "本社团成员";
                }else if(jRadioButton3.isSelected()){
                    range = "全体学生";
                }
            ps.setString(6, range);
            ps.setInt(7, Integer.parseInt(jTextField6.getText()));
            
            int status = 0;
            if(jRadioButton1.isSelected()){
                status = 0;
            }if(jRadioButton4.isSelected()){
                status = 1;
            }if(jRadioButton5.isSelected()){
                status = 2;
            }
            ps.setInt(8, status);
            ps.setString(9, name);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"数据修改成功");
            
            jTable1.setModel(new DefaultTableModel(new Object[]{"活动名称","主办方","活动时间","活动地址","活动内容","报名范围","最大人数","活动状态"},0));
            TableFunction.fillActivityTable(jTable1, "");
        } catch (SQLException ex) {
            Logger.getLogger(Manage_activity_form.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

